import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs';
import { join } from 'path';

import semver from 'semver';

const CONSTANTS_DIR =
  'packages/twenty-server/src/engine/core-modules/upgrade/constants';
const UPGRADE_COMMANDS_DIR =
  'packages/twenty-server/src/database/commands/upgrade-version-command';
const WORKSPACE_COMMAND_PROVIDER_PATH =
  `${UPGRADE_COMMANDS_DIR}/workspace-command-provider.module.ts`;

function parseVersion(version: string): semver.SemVer {
  const parsed = semver.parse(version);

  if (!parsed) {
    throw new Error(`Invalid version format: ${version}`);
  }

  return parsed;
}

function versionToDir(version: string): string {
  const { major, minor } = parseVersion(version);

  return `${major}-${minor}`;
}

function incrementMinorVersion(version: string): string {
  const incremented = semver.inc(version, 'minor');

  if (!incremented) {
    throw new Error(`Failed to increment version: ${version}`);
  }

  return incremented;
}

function getCurrentVersion(): string {
  const filePath = join(CONSTANTS_DIR, 'twenty-current-version.constant.ts');
  const content = readFileSync(filePath, 'utf-8');
  const match = content.match(/'([0-9.]+)'/);

  if (!match) {
    throw new Error('Could not extract current version');
  }

  return match[1];
}

function getPreviousVersions(): string[] {
  const filePath = join(CONSTANTS_DIR, 'twenty-previous-versions.constant.ts');
  const content = readFileSync(filePath, 'utf-8');
  const matches = content.match(/'[0-9.]+'/g);

  if (!matches) {
    return [];
  }

  return matches.map((m) => m.replace(/'/g, ''));
}

function getNextVersions(): string[] {
  const filePath = join(CONSTANTS_DIR, 'twenty-next-versions.constant.ts');
  const content = readFileSync(filePath, 'utf-8');
  const matches = content.match(/'[0-9.]+'/g);

  if (!matches) {
    return [];
  }

  return matches.map((m) => m.replace(/'/g, ''));
}

const AUTO_GENERATED_HEADER = `/*
 * _____                    _
 *|_   _|_      _____ _ __ | |_ _   _
 *  | | \\ \\ /\\ / / _ \\ '_ \\| __| | | | Auto-generated file
 *  | |  \\ V  V /  __/ | | | |_| |_| | Any edits to this will be overridden
 *  |_|   \\_/\\_/ \\___|_| |_|\\__|\\__, | Generated by: scripts/bump-version.ts
 *                              |___/
 */

`;


function updateCurrentVersion(newVersion: string): void {
  const filePath = join(CONSTANTS_DIR, 'twenty-current-version.constant.ts');

  writeFileSync(
    filePath,
    `${AUTO_GENERATED_HEADER}export const TWENTY_CURRENT_VERSION = '${newVersion}' as const;\n`,
  );

  console.log(`Updated TWENTY_CURRENT_VERSION to '${newVersion}'`);
}

function updatePreviousVersions(versions: string[]): void {
  const filePath = join(CONSTANTS_DIR, 'twenty-previous-versions.constant.ts');
  const formattedVersions = versions.map((v) => `  '${v}',`).join('\n');

  writeFileSync(
    filePath,
    `${AUTO_GENERATED_HEADER}export const TWENTY_PREVIOUS_VERSIONS = [\n${formattedVersions}\n] as const;\n`,
  );

  console.log(`Updated TWENTY_PREVIOUS_VERSIONS with ${versions.length} versions`);
}

function updateNextVersions(versions: string[]): void {
  const filePath = join(CONSTANTS_DIR, 'twenty-next-versions.constant.ts');
  const formattedVersions = versions.map((v) => `'${v}'`).join(', ');

  writeFileSync(
    filePath,
    `${AUTO_GENERATED_HEADER}export const TWENTY_NEXT_VERSIONS = [${formattedVersions}] as const;\n`,
  );

  console.log(`Updated TWENTY_NEXT_VERSIONS to [${formattedVersions}]`);
}

function createUpgradeCommandModule(version: string): void {
  const dirName = versionToDir(version);
  const dirPath = join(UPGRADE_COMMANDS_DIR, dirName);

  if (existsSync(dirPath)) {
    console.log(`Upgrade command directory '${dirName}' already exists, skipping`);

    return;
  }

  mkdirSync(dirPath, { recursive: true });

  const [major, minor] = dirName.split('-');
  const className = `V${major}_${minor}_UpgradeVersionCommandModule`;
  const moduleContent = `import { Module } from '@nestjs/common';

@Module({
  imports: [],
  providers: [],
})
export class ${className} {}
`;

  writeFileSync(
    join(dirPath, `${dirName}-upgrade-version-command.module.ts`),
    moduleContent,
  );

  console.log(`Created upgrade command module for version ${version}`);

  registerModuleInProvider(version, dirName, className);
}

function registerModuleInProvider(
  version: string,
  dirName: string,
  className: string,
): void {
  const content = readFileSync(WORKSPACE_COMMAND_PROVIDER_PATH, 'utf-8');

  const importStatement = `import { ${className} } from 'src/database/commands/upgrade-version-command/${dirName}/${dirName}-upgrade-version-command.module';`;

  const lastImportMatch = content.match(
    /import { V\d+_\d+_UpgradeVersionCommandModule }[^;]+;/g,
  );

  if (!lastImportMatch) {
    throw new Error('Could not find import statements in workspace-command-provider.module.ts');
  }

  const lastImport = lastImportMatch[lastImportMatch.length - 1];
  const contentWithImport = content.replace(
    lastImport,
    `${lastImport}\n${importStatement}`,
  );

  const importsArrayMatch = contentWithImport.match(
    /imports: \[\n([\s\S]*?)\n  \]/,
  );

  if (!importsArrayMatch) {
    throw new Error('Could not find imports array in workspace-command-provider.module.ts');
  }

  const currentImports = importsArrayMatch[1];
  const updatedImports = `${currentImports},\n    ${className}`;
  const finalContent = contentWithImport.replace(
    importsArrayMatch[0],
    `imports: [\n${updatedImports}\n  ]`,
  );

  writeFileSync(WORKSPACE_COMMAND_PROVIDER_PATH, finalContent);

  console.log(`Registered ${className} in workspace-command-provider.module.ts`);
}

function bumpVersion(newVersion: string): void {
  parseVersion(newVersion);

  const currentVersion = getCurrentVersion();
  const previousVersions = getPreviousVersions();
  const nextVersions = getNextVersions();

  console.log('\nCurrent state:');
  console.log(`  TWENTY_CURRENT_VERSION: '${currentVersion}'`);
  console.log(`  TWENTY_PREVIOUS_VERSIONS: [${previousVersions.map((v) => `'${v}'`).join(', ')}]`);
  console.log(`  TWENTY_NEXT_VERSIONS: [${nextVersions.map((v) => `'${v}'`).join(', ')}]`);

  const updatedPreviousVersions = [...previousVersions, currentVersion];

  const nextNextVersion = incrementMinorVersion(newVersion);

  console.log('\nApplying changes:');

  updatePreviousVersions(updatedPreviousVersions);
  updateCurrentVersion(newVersion);
  updateNextVersions([nextNextVersion]);
  createUpgradeCommandModule(nextNextVersion);

  console.log('\nNew state:');
  console.log(`  TWENTY_CURRENT_VERSION: '${newVersion}'`);
  console.log(`  TWENTY_PREVIOUS_VERSIONS: [${updatedPreviousVersions.map((v) => `'${v}'`).join(', ')}]`);
  console.log(`  TWENTY_NEXT_VERSIONS: ['${nextNextVersion}']`);
  console.log('\nVersion bump complete!');
}

const newVersion = process.argv[2];

if (!newVersion) {
  console.error('Usage: tsx scripts/bump-version.ts <new-version>');
  console.error('Example: tsx scripts/bump-version.ts 2.4.0');
  process.exit(1);
}

bumpVersion(newVersion);
