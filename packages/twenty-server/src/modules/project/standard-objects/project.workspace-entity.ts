/**
 * Project workspace entity for Egyptian real estate developers
 */
export class ProjectWorkspaceEntity {
  // ========== BASE FIELDS (auto-generated by Twenty) ==========
  id: string;
  createdAt: string;
  updatedAt: string;
  deletedAt: string | null;
  
  // ========== CORE FIELDS ==========
  /**
   * Official name of the development project
   * Example: "Madinaty", "Beverly Hills", "Fifth Settlement Compound"
   */
  name: string | null;
  
  /**
   * Internal reference code
   * Example: "PRJ-2026-001"
   */
  code: string | null;
  
  /**
   * Developer company (linked to Companies object)
   */
  developerId: string | null;
  developer: ActorMetadata;
  
  // ========== LOCATION ==========
  /**
   * Area or district
   * Examples: "New Cairo", "Sheikh Zayed", "North Coast", "East Owainat"
   */
  location: string | null;
  
  /**
   * Full street address
   */
  address: string | null;
  
  /**
   * GPS coordinates for map integration
   * Format: "30.0284,31.4913" (latitude,longitude)
   */
  gpsCoordinates: string | null;
  
  // ========== TIMELINE ==========
  /**
   * Construction start date
   */
  startDate: string | null;
  
  /**
   * Official sales launch date
   */
  launchDate: string | null;
  
  /**
   * Contractual delivery date to buyers
   */
  deliveryDate: string | null;
  
  /**
   * Actual delivery date (when handed over to owners)
   */
  actualDeliveryDate: string | null;
  
  // ========== FINANCIALS (Egyptian Pound) ==========
  /**
   * Total development budget in EGP
   */
  totalBudget: CurrencyMetadata | null;
  
  /**
   * Expected total sales value in EGP
   */
  totalValue: CurrencyMetadata | null;
  
  // ========== STATUS ==========
  /**
   * Project status
   * Values: planning, inProgress, launched, delivered, onHold, cancelled
   */
  status: string | null;
  
  /**
   * Construction completion percentage (0-100)
   */
  completionPercentage: number | null;
  
  // ========== DETAILS ==========
  /**
   * Project overview and key features (HTML/Rich text)
   */
  description: string | null;
  
  /**
   * Total land area in square meters
   */
  totalArea: number | null;
  
  /**
   * Total number of residential/commercial units
   */
  numberOfUnits: number | null;
  
  /**
   * Number of separate buildings in the project
   */
  numberOfBuildings: number | null;
  
  /**
   * Project type classification
   * Values: residential, commercial, mixed, compound, administrative
   */
  projectType: string | null;
  
  /**
   * Search tags for filtering
   * Examples: ["New Cairo", "Luxury", "Golf View", "Clubhouse"]
   */
  tags: string[] | null;
  
  // ========== RELATIONS ==========
  /**
   * All properties/units in this project
   * Relation: ONE_TO_MANY with PropertyWorkspaceEntity
   */
  properties: any[];
  
  /**
   * All reservations for properties in this project
   * Relation: ONE_TO_MANY with ReservationWorkspaceEntity
   */
  reservations: any[];
  
  // ========== SEARCH ==========
  /**
   * Full-text search vector (auto-generated)
   */
  searchVector: string;
}