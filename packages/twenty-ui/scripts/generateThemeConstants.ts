// Generates a static TypeScript file with pre-computed theme constants.
// These values are string literals with no runtime imports, so wyw-in-js
// can evaluate them in its restricted sandbox without triggering the
// twenty-ui dist bundle's dependency chain (safe-regex-test / get-intrinsic).
//
// Usage (from workspace root):
//   npx tsx packages/twenty-ui/scripts/generateThemeConstants.ts
//
// Prerequisites: twenty-ui must be built first (npx nx build twenty-ui).

import { createRequire } from 'node:module';
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);

const {
  themeCssVariables,
  MOBILE_VIEWPORT,
  GRAY_SCALE_LIGHT,
} = require('../dist/theme.cjs');

const serializeObject = (
  obj: Record<string, unknown>,
  indent = 2,
): string => {
  const spaces = ' '.repeat(indent);
  const entries = Object.entries(obj).map(([key, value]) => {
    const safeKey = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key)
      ? key
      : JSON.stringify(key);

    if (typeof value === 'object' && value !== null) {
      return `${spaces}${safeKey}: ${serializeObject(value as Record<string, unknown>, indent + 2)},`;
    }
    return `${spaces}${safeKey}: ${JSON.stringify(value)},`;
  });

  return `{\n${entries.join('\n')}\n${' '.repeat(indent - 2)}}`;
};

const grayScaleEntries = Object.entries(
  GRAY_SCALE_LIGHT as Record<string, string>,
)
  .map(([key, value]) => `  ${key}: ${JSON.stringify(value)},`)
  .join('\n');

const serializedThemeCssVariables = serializeObject(themeCssVariables);

const fileContent = `\
// Auto-generated by scripts/generateThemeConstants.ts — do not edit manually.
// Regenerate: npx tsx packages/twenty-ui/scripts/generateThemeConstants.ts
//
// Static copy of theme values that can be evaluated by wyw-in-js at build time
// without triggering the twenty-ui dist bundle's problematic dependency chain.

import type { ThemeType } from '@ui/theme/types/ThemeType';

type DeepCSSVariableRefs<T> = {
  [K in keyof T]: T[K] extends (...args: never[]) => unknown
    ? Record<string | number, string>
    : T[K] extends Record<string, unknown>
      ? DeepCSSVariableRefs<T[K]>
      : string;
};

export const MOBILE_VIEWPORT = ${MOBILE_VIEWPORT};

export const GRAY_SCALE_LIGHT = {
${grayScaleEntries}
} as const;

export const themeCssVariables = ${serializedThemeCssVariables} as DeepCSSVariableRefs<ThemeType>;
`;

const outputDir = resolve(scriptDir, '../src/theme-constants');
mkdirSync(outputDir, { recursive: true });

const outputPath = resolve(outputDir, 'ThemeCssVariablesStatic.ts');
writeFileSync(outputPath, fileContent, 'utf-8');
console.log('Generated', outputPath);
